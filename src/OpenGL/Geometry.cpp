std::unique_ptr<Geometry> GeometryFactory::createGeometry(GeometryType geometryType) {
    if (geometryType == SPHERE) {
        return std::make_unique<Sphere>();
    } else if (geometryType == CUBE) {
        return std::make_unique<Cube>();
    } else if (geometryType == QUAD) {
        return std::make_unique<Quad>();
    } else if (geometryType == PLANE) {
        return std::make_unique<Plane>();
    } else {
        std::cerr << "Unknown geometry type" << std::endl;
        return nullptr;
    }
}